# coding=utf-8
# This is a sample Python script.

import turtle as t
import time

# 初始化画布
t.setup(width=800, height=600)

time.sleep(2)
t.hideturtle()
t.color("#FF6A6A")
t.penup()
t.goto(-300,220)
words1 = '当 '
words2 = ['送','给','胡','洪','波']
for i in range(4):
    t.write(words1, font=("华文彩云", 3*i+14, "bold"))
    t.forward(20+3*i)
    time.sleep(0.15)
t.forward(8)
t.write("！", font=("华文彩云", 23, "bold"))
t.forward(20)
time.sleep(0.5)
for word in words2:
    t.write(word, font=("华文彩云", 26, "bold"))
    t.forward(35)
    time.sleep(0.4)
t.write("：", font=("华文彩云", 23, "bold"))
t.forward(35)
#大白
#头
t.goto(-100,150)
t.right(90)
t.color("black")
t.pensize(1)
t.pendown()
size = 0.6
a = 0.8*size
for i in range(120):
    if 0 <= i < 30 or 60 <= i < 90:
        a = a + 0.2*size
        t.left(3)  # 向左转3度
        t.forward(a)  # 向前走a的步长
    else:
        a = a - 0.2*size
        t.left(3)
        t.forward(a)
t.penup()
#眼睛
time.sleep(0.5)
t.goto(-70,150)
t.dot(14)
time.sleep(0.5)
t.goto(-24,150)
t.dot(14)
time.sleep(0.5)
t.right(60)
t.pendown()
t.speed(2)
t.circle(-50,50)
#身体
t.penup()
t.goto(-89.85,131.47)
t.left(65)
t.pendown()
t.circle(250,60)
t.penup()
t.goto(-2.64,133.09)
t.left(30)
t.pendown()
t.circle(-250,60)
t.right(20)
t.circle(-134,110)
#腿
t.penup()
t.left(135)
t.goto(-120,-145)
t.pendown()
t.circle(120,45)
t.left(15)
t.circle(42,80)
t.left(50)
t.forward(70)
t.penup()
t.back(70)
t.pendown()
t.right(130)
t.circle(42,80)
t.left(15)
t.circle(115,48)
t.penup()
#胳膊
t.goto(-125,89)
t.pendown()
t.left(112)
t.circle(250,50)
t.circle(50,45)
t.circle(20,80)
t.circle(50,45)
t.circle(180,16)
t.penup()
t.goto(31,90)
t.pendown()
t.right(126)
t.circle(-250,50)
t.circle(-50,45)
t.circle(-20,80)
t.circle(-50,45)
t.circle(-180,14)
#手
t.penup()
t.goto(-208,-90)
t.pendown()
t.left(150)
t.right(180)
t.circle(-30,20)
t.penup()
t.goto(-208,-90)
t.right(160)
t.pendown()
t.circle(30,40)
t.circle(5,120)
t.circle(40,40)


#画玫瑰
t.penup()
t.goto(-270,60)
t.pendown()
#玫瑰花角度
t.right(60)
#玫瑰花大小
size=0.35
# 花蕊
t.fillcolor("#EE0000")
t.begin_fill()
t.circle(10*size, 180)
t.circle(25*size, 110)
t.left(50)
t.circle(60*size, 45)
t.circle(20*size, 170)
t.right(24)
t.forward(30*size)
t.left(10)
t.circle(30*size, 110)
t.forward(20*size)
t.left(40)
t.circle(90*size, 70)
t.circle(30*size, 150)
t.right(30)
t.forward(15*size)
t.circle(80*size, 90)
t.left(15)
t.forward(45*size)
t.right(165)
t.forward(20*size)
t.left(155)
t.circle(150*size, 80)
t.left(50)
t.circle(150*size, 90)
t.end_fill()
# 花瓣1
t.left(150)
t.circle(-90*size, 70)
t.left(20)
t.circle(75*size, 105)
t.setheading(80)
t.circle(80*size, 98)
t.circle(-90*size, 40)
# 花瓣2
t.left(180)
t.circle(90*size, 40)
t.circle(-80*size, 98)
t.setheading(-63.5)
# 叶子1
t.forward(30*size)
t.left(90)
t.forward(25*size)
t.left(45)
t.fillcolor("#00CD00")
t.begin_fill()
t.circle(-80*size, 90)
t.right(90)
t.circle(-80*size, 90)
t.end_fill()
t.right(135)
t.forward(60*size)
t.left(180)
t.penup()
t.forward(85*size)
t.pendown()
t.left(90)
t.forward(80*size)
# 叶子2
t.right(90)
t.right(45)
t.fillcolor("#00CD00")
t.begin_fill()
t.circle(80*size, 90)
t.left(90)
t.circle(80*size, 90)
t.end_fill()
t.left(135)
t.forward(60*size)
t.left(180)
t.forward(60*size)
t.right(90)
t.circle(-1000*size,8.6)
t.penup()
t.goto(-200,-110)
t.pendown()
t.circle(-1000*size,10)

#圣诞快乐
words3 = ['爱 ','你 ','哦 ''!']
t.penup()
t.color("#FF6A6A")
t.goto(30,220)
t.setheading(0)
for word in words3:
    t.write(word, font=("华文彩云", 26, "bold"))
    t.forward(35)
    time.sleep(0.4)
t.done()