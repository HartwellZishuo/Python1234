import turtle   as  t
#设置窗口大小和位置，大小800*600，左上角坐标：x方向100，y方向50
t.setup(800,600,100,50)
#设置标题栏文字
t.title("画大白，04组：")
#设置画布大小和颜色
t.screensize(680,480,"lightpink")
#画圆.画坐标原点，向下移动笔,移动的距离是圆的半径
t.penup()
#移动笔,画原点
t.color("red")#设置颜色
t.goto(0,-5)
t.pendown()
t.begin_fill()#开始填充
t.circle(5)
t.end_fill()#结束填充
t.color("black")
#画向右的x轴，回到原点
t.penup()
t.goto(0,0)
t.pendown()
#向右移动300
t.goto(300,0)
#画箭头
t.stamp()
#画向上的y轴
t.penup()
t.goto(0,0)
t.pendown()
t.left(90)
t.goto(0,200)
#画箭头
t.stamp()
#画向左的箭头
t.penup()
t.goto(0,0)
t.pendown()
t.left(90)
#向左移动300
t.goto(-300,0)
#画箭头
t.stamp()
#画向下的y轴
t.penup()
t.goto(0,0)
t.pendown()
t.left(90)
t.goto(0,-200)
t.stamp()

#启动消息循环，保证界面窗口呈现
t.mainloop()